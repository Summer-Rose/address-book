public class Birthday {
  private String mMonth;
  private int mDay;
  private int mYear;

  public Birthday(String month, int day, int year) {
    mMonth = month;
    mDay = day;
    mYear = year;
  }

  public String getMonth() {
    return mMonth;
  }

  public int getDay() {
    return mDay;
  }

  public int getYear() {
    return mYear;
  }
}
